
#' Multiplicar Dois Numeros
#'
#' \code{multiplicar()} retorna a multiplicação entre \code{x} e \code{y}.
#'
#' @param x Um numero
#' @param y Um nmero
#'
#' @return A multiplicação de \code{x} e \code{y}
#'
#' @examples
#' # Multiplicar 2*2 = 4
#' multiplicar(2,2)
#'
#' @export

multiplicar<- function(x,y){
  x*y
}
